/**
 * 标签
 */
package com.cloud.dis.iface.stream.request;

public class Tag {
	
	/**
	 * 键，最大长度36个unicode字符
	 */
	private String key;
	
	/**
	 * 值，最大长度为43个unicode字符
	 */
	private String value;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
